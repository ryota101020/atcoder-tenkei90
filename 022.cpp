#include <bits/stdc++.h>
using namespace std;
#define rep(i, n) for (int i = 0; i < (int)n; ++i)
#define repi(i, a, b) for (int i = int(a); i < int(b); ++i)
#define repr(i, n) for (int i = (int)n - 1; i >= 0; --i)
#define ALL(a) (a).begin(), (a).end()
#define ll long long

ll gcd(ll a, ll b) {
  if (a % b == 0) {
    return b;
  } else {
    return gcd(b, a % b);
  }
}

int main() {
  ll a, b, c;
  cin >> a >> b >> c;
  ll tmp = gcd(gcd(a, b), c);
  cout << (a / tmp - 1) + (b / tmp - 1) + (c / tmp - 1) << endl;
  return 0;
}
